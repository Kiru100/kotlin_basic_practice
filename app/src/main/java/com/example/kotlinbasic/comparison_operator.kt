package com.example.kotlinbasic

fun main(){
    //Comparison operators (==, !=, <, >, <=, >=)

    //Equal
    val isEqual = 5 == 5
    println("Value isEqual is || result: $isEqual")

    //not Equal
    val isNotEqual = 5!=5

    println("Value isNotEqual is || result: $isNotEqual")

