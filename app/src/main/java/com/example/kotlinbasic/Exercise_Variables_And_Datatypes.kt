package com.example.kotlinbasic

class Exercise_Variables_And_Datatypes {

    //String
    var myString:String ="Android Masterclass"
    //Float should be val because 13.37 should not be changed since it's a constant value
    val myFloat:Float=13.37F
    //Double should be val because 3.14 should not be changed since it's a constant value
    val myDouble:Double=3.14159265358979
    //Byte
    var myByte:Byte=25
    //Short
    var myShort:Short=2020
    //Int
    var myInt: Long =18881234567
    //boolean
    var myBoolean: Boolean=true
    //char
    var myChar: Char='a'
}